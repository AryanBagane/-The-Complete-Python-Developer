# Fundamental data types

# Integer and Float
print(2 + 4)
print(2 * 4)
print(2 - 4)
print(2 / 4)
print(type(2 + 4))
print(type(2 / 4))
print(type(0))
print(type(0.0))
print(2 ** 6)
print(5 // 2)
print(5 % 2)
print(type(2 ** 6))
print(type(5 // 2))
print(type(5 % 2))

# math functions
